        <p><strong>Với câu hỏi được đăng tải bởi Người Dùng:</strong></p>
        <ul>
            <li>Câu hỏi chỉ bao gồm emoticon hoặc có số lượng emoticon lớn hơn số lượng ký tự văn bản.</li>
            <li>Câu hỏi có nội dung không xác định, không liên quan đến IT (ví dụ: nội dung quảng cáo, thương
                mại,...)</li>
            <li>Câu hỏi mang tính chất kích động, cổ vũ bạo lực, tấn công vào một cá nhân hoặc tổ chức khác.
            </li>
            <li>Các câu hỏi không mang giá trị học hỏi, trao đổi lẫn nhau hay xây dựng và phát triển tích cực
