        <p>4. Người Dùng trong quá trình sử dụng dịch vụ thì không được phép thực hiện những hành vi xây dựng
            nội dung không phù hợp với quy định của chúng tôi, bao gồm nhưng không giới hạn những hành vi như
            sau:</p>
        <p><strong>Với bài viết được đăng tải bởi Người Dùng:</strong></p>
        <ul>
            <li>Bài viết quá ngắn (dưới 150 từ / 1000 ký tự bao gồm cả dấu cách) hoặc chỉ bao gồm những từ ngữ
                mang tính chất cảm thán: khùng, điên, chán, ừ, đồng ý,...</li>
            <li>Bài viết chỉ có biểu tượng cảm xúc (emotion icon) hoặc số lượng ký tự emoticon nhiều hơn số ký
