            <li>Bài viết có nội dung không xác định, vô nghĩa hoặc không diễn tả được điều cần hướng tới.</li>
            <li>Bài viết có nội dung không liên quan tới tiêu đề và ngược lại, hoặc chỉ có nội dung quảng cáo.
            </li>
            <li>Bài viết có nội dung hoặc link URL mang bản chất quảng cáo hoặc thương mại, lặp đi lặp lại,
                không liên quan hoặc quá dài dòng.</li>
            <li>Bài viết có nội dung không liên quan đến CNTT, không truyền tải thông tin có giá trị và đóng góp
                cho cộng đồng CNTT Việt.</li>
            <li>Bài viết sử dụng nhiều từ lóng, ngôn từ, hình ảnh gây phản cảm, không phù hợp với thuần phong mỹ
                tục, không giữ gìn sự trong sáng của Tiếng Việt.</li>
            <li>Bài viết có dấu hiệu sao chép, đạo văn, trích dẫn nguồn mà không được sự cho phép của tác giả
                nội dung đó. Sau khi Ban Quản trị xác nhận, nếu khối lượng nội dung có dấu hiệu sao chép vượt
                quá 20% trên tổng bài viết, bài viết đó sẽ được coi là Spam.</li>
