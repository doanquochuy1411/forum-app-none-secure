                vụ này.</li>
            <li>Hành vi sử dụng ID Người Dùng gây khó chịu và hiểu nhầm cho người khác. Hành vi sử dụng ID Người
                Dùng giống, hoặc gần giống với tên những tổ chức hay sản phẩm nổi tiếng được chúng tôi hay nhiều
                người khác biết đến.</li>
            <li>Hành vi thu thập, đánh cắp, lưu trữ, chỉnh sửa, sử dụng thông tin cá nhân. Hành vi thay đổi
                thông tin cá nhân, thông tin người dùng của bản thân một cách bất chính.</li>
            <li>Hành vi làm ẩn đi những thành phần cơ bản của dịch vụ như Header, Footer, Quảng cáo, hay các ký
                hiệu Bản quyền tác giả... mà không được sự cho phép của chúng tôi.</li>
            <li>Hành vi buôn bán, chuyển nhượng dịch vụ mà không được sự đồng ý của chúng tôi.</li>
