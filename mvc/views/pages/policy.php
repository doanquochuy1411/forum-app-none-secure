            <li>Bình luận/Câu trả lời có nội dung trùng lặp trong cùng một chủ đề.</li>
            <li>Bình luận/trả lời mang tính công kích cá nhân tổ chức, ngôn từ không phù hợp với thuần phong mỹ
                tục.</li>
        </ul>
        <p>Người Dùng, ngoài những điều được kể trên ra, thì cũng không được phép thực hiện những hành động được
