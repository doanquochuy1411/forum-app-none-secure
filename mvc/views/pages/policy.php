        <h2>Điều 8: Xử phạt tài khoản và nội dung vi phạm</h2>
        <p>Quy định xử lý các Vi phạm những hành vi Cấm được quy định tại Điều 7 nêu trên:</p>
        <p>Tạm khóa tài khoản trong vòng 3 ngày, 1 tuần, 2 tuần, 1 tháng hoặc xóa tài khoản vĩnh viễn, tùy theo
            mức độ nghiêm trọng của hành vi. Ban Quản trị dịch vụ có quyền xem xét và đưa ra quyết định thực thi
